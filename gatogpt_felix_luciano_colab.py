def build_messages(user_message, history):
    messages = [{"role": "system", "content": FELIX_SYSTEM_PROMPT}]

    for user_text, assistant_text in history[-8:]:
        if user_text:
            messages.append({"role": "user", "content": user_text})
        if assistant_text:
            messages.append({"role": "assistant", "content": assistant_text})

    messages.append({"role": "user", "content": user_message})

    return messages


def felix_chat_response(user_message, history):
    load_chat_model()

    messages = build_messages(user_message, history)

    chat_text = chat_tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
        enable_thinking=False,
    )

    inputs = chat_tokenizer(
        [chat_text],
        return_tensors="pt"
    ).to(chat_model.device)

    outputs = chat_model.generate(
        **inputs,
        max_new_tokens=450,
        temperature=0.7,
        top_p=0.85,
        top_k=20,
        do_sample=True,
        pad_token_id=chat_tokenizer.eos_token_id,
    )

    new_tokens = outputs[0][inputs.input_ids.shape[-1]:]
    response = chat_tokenizer.decode(
        new_tokens,
        skip_special_tokens=True
    ).strip()

    if not response:
        response = "Miau... me quedé observando la idea, pero no encontré una respuesta clara. ¿Me das un poco más de contexto? 🐾"

    return f"🐱 Félix: {response}"


def clean_prompt(command_text, command_name):
    prompt = re.sub(
        rf"^\s*{re.escape(command_name)}\s*",
        "",
        command_text,
        flags=re.IGNORECASE
    ).strip()

    if prompt:
        return prompt

    return "un gato digital inteligente llamado Félix estudiando con Luciano, estilo ilustración bonita"


def felix_image_prompt(prompt):
    return (
        f"{prompt}. Cute black and white digital cat assistant named Felix, "
        "big expressive eyes, friendly educational companion, cozy study mood, "
        "high quality illustration, soft lighting"
    )


def generate_image(prompt):
    load_text_to_image_pipeline() # Usar la nueva función de carga

    final_prompt = felix_image_prompt(prompt)

    image = text_to_image_pipeline( # Usar el nuevo pipeline
        prompt=final_prompt,
        num_inference_steps=2,
        guidance_scale=0.0,
        height=512,
        width=512,
    ).images[0]

    filename = f"gatimage_{datetime.now().strftime('%Y%m%d_%H%M%S')}.png"
    path = os.path.join(OUTPUT_DIR, filename)

    image.save(path)

    unload_text_to_image_pipeline() # Descargar el modelo después de usarlo

    return path


def draw_caption_frame(image, caption):
    # Asegurar que la imagen sea 512x512 para el captioning consistente
    frame = image.copy().resize((512, 512))
    draw = ImageDraw.Draw(frame)

    text = caption[:90]
    box_height = 70

    draw.rectangle(
        (0, 512 - box_height, 512, 512),
        fill=(0, 0, 0)
    )

    draw.text(
        (16, 512 - box_height + 18),
        text,
        fill=(255, 255, 255),
        font=ImageFont.load_default()
    )

    return frame


def generate_video(prompt):
    import imageio.v2 as imageio

    # 1. Asegurarse de que otros modelos pesados estén descargados para liberar VRAM
    unload_chat_model()
    unload_stable_video_pipeline()
    unload_text_to_image_pipeline() # Limpiar antes de cargar temporalmente

    # 2. Generar una imagen base de alta calidad usando el modelo de texto a imagen
    print("🎨 Generando imagen base para el video SVD (esto puede tardar un poco, miau)...")
    load_text_to_image_pipeline() # Cargar el pipeline de texto a imagen temporalmente
    initial_image_prompt = felix_image_prompt(f"{prompt}, still frame, cinematic, professional, high quality, centered, no blurry parts")
    initial_image = text_to_image_pipeline(
        prompt=initial_image_prompt,
        num_inference_steps=4, # Aumentar pasos para una mejor calidad de imagen base
        guidance_scale=0.0,
        height=576, # Resoluciones recomendadas para SVD
        width=576,
    ).images[0]

    unload_text_to_image_pipeline() # Descargar el modelo T2I para liberar VRAM para SVD

    # 3. Cargar y usar el modelo Stable Video Diffusion
    load_stable_video_pipeline() # Cargar el pipeline SVD

    print("🎥 Generando video con Stable Video Diffusion (¡paciencia, esto es lo más ronroneante!)...")

    # Generar los frames del video
    frames = stable_video_pipeline(
        initial_image,
        num_frames=25, # SVD-XT genera típicamente 25 frames
        decode_chunk_size=8,
        motion_adapter_alpha=0.5, # Parámetro específico para la fuerza del movimiento en SVD-XT
    ).frames[0] # SVD devuelve frames como una lista de imágenes PIL

    captioned_frames = []
    video_caption = f"GatoGPT Félix: {prompt}"
    for i, frame in enumerate(frames):
        # Redimensionar el frame de salida de SVD a 512x512 para un captioning consistente
        frame_resized_for_caption = frame.resize((512, 512))
        captioned_frames.append(draw_caption_frame(frame_resized_for_caption, video_caption))


    filename = f"gativeo_{datetime.now().strftime('%Y%m%d_%H%M%S')}.mp4"
    path = os.path.join(OUTPUT_DIR, filename)

    # Guardar el video con una velocidad de fotogramas adecuada (ej. 8-10 FPS para 25 frames)
    imageio.mimsave(path, captioned_frames, fps=8)

    # 4. Descargar el modelo Stable Video Diffusion después de usarlo
    unload_stable_video_pipeline()

    return path


def respond(user_message, history):
    if not user_message or not user_message.strip():
        return history, "", None, None

    lower = user_message.strip().lower()

    if lower.startswith("@gatimage"):
        prompt = clean_prompt(user_message, "@gatimage")
        image_path = generate_image(prompt)

        reply = (
            "🐱 Félix: Prrr... usé mi imaginación felina local para crear una imagen. "
            "La dejé lista aquí abajo, miau. 🐾"
        )

        history = history + [(user_message, reply)]

        return history, "", image_path, None

    if lower.startswith("@gativeo"):
        prompt = clean_prompt(user_message, "@gativeo")
        video_path = generate_video(prompt)

        reply = (
            "🐱 Félix: ¡Miau! Usé el modelo Stable Video Diffusion para crear un video de alta calidad. "
            "Espero que te guste el movimiento, Luciano. 🐾"
        )

        history = history + [(user_message, reply)]

        return history, "", None, video_path

    reply = felix_chat_response(user_message, history)

    history = history + [(user_message, reply)]

    return history, "", None, None


def build_app():
    with gr.Blocks(
        title="GatoGPT Félix",
        theme=gr.themes.Soft()
    ) as demo:

        gr.Markdown(
            """
            # 🐱 GatoGPT Félix

            Chat local estilo ChatGPT con personalidad felina.

            **Comandos especiales:**

            - `@gatimage un gato estudiando matemáticas` → genera imagen local.
            - `@gativeo Félix caminando por una biblioteca mágica` → genera video local de alta calidad.
            """
        )

        chatbot = gr.Chatbot(
            height=430,
            label="Chat con Félix"
        )

        message = gr.Textbox(
            label="Escribe a Félix",
            placeholder="Pregúntame algo, o usa @gatimage / @gativeo...",
            lines=2,
        )

        with gr.Row():
            send = gr.Button(
                "Enviar 🐾",
                variant="primary"
            )
            clear = gr.Button("Limpiar chat")

        with gr.Row():
            image_output = gr.Image(
                label="Imagen generada por @gatimage",
                type="filepath"
            )

            video_output = gr.Video(
                label="Video generado por @gativeo"
            )

        send.click(
            respond,
            [message, chatbot],
            [chatbot, message, image_output, video_output]
        )

        message.submit(
            respond,
            [message, chatbot],
            [chatbot, message, image_output, video_output]
        )

        clear.click(
            lambda: ([], None, None),
            None,
            [chatbot, image_output, video_output]
        )

        return demo


