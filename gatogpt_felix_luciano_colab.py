def unload_chat_model():
    global chat_model, chat_tokenizer
    chat_model = None
    chat_tokenizer = None
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

def unload_text_to_image_pipeline(): # Renombrada
    global text_to_image_pipeline
    text_to_image_pipeline = None
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

def unload_stable_video_pipeline(): # Nueva función
    global stable_video_pipeline
    stable_video_pipeline = None
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()


def load_chat_model():
    global chat_tokenizer, chat_model

    if chat_model is not None and chat_tokenizer is not None:
        return

    print("🐱 Cargando modelo de chat local...")
    print("Esto puede tardar la primera vez.")

    # Asegurar que otros modelos pesados estén descargados
    unload_text_to_image_pipeline()
    unload_stable_video_pipeline()

    chat_tokenizer = AutoTokenizer.from_pretrained(CHAT_MODEL_ID)

    chat_model = AutoModelForCausalLM.from_pretrained(
        CHAT_MODEL_ID,
        torch_dtype=DTYPE,
        device_map="auto" if torch.cuda.is_available() else None,
    )

    if not torch.cuda.is_available():
        chat_model.to(DEVICE)

    print("✅ Modelo de chat cargado.")


def load_text_to_image_pipeline(): # Renombrada
    global text_to_image_pipeline

    if text_to_image_pipeline is not None:
        return

    print("🎨 Cargando modelo local de imagen (Text-to-Image)...")
    print("Esto solo ocurre cuando usas @gatimage o para la imagen base de @gativeo.")

    # Asegurar que otros modelos pesados estén descargados
    unload_chat_model()
    unload_stable_video_pipeline()

    from diffusers import AutoPipelineForText2Image # Importación movida aquí

    text_to_image_pipeline = AutoPipelineForText2Image.from_pretrained(
        TEXT_TO_IMAGE_MODEL_ID, # Usar la nueva variable
        torch_dtype=DTYPE,
        variant="fp16" if torch.cuda.is_available() else None,
    )

    text_to_image_pipeline.to(DEVICE)

    print("✅ Modelo de imagen cargado.")


